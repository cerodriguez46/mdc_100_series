    return Scaffold(
      // TODO: Add app bar (102)
      //this adds an app bar or navigation bar in ios
      appBar: AppBar(
        // TODO: Add buttons and title (102)
        //icon for menu added but put before it will put before text
        leading: IconButton(
          icon: Icon(
            Icons.menu,
            semanticLabel: 'menu',
          ),
          onPressed: () {
            print('Menu button');
          },
        ),
        //this adds the title to the app bar
        title: Text('SHRINE'),

        //Added trailing buttons called actions or specifically for settings
        //as well as search icon
        actions: <Widget>[
          IconButton(
            icon: Icon(
              Icons.search,
              semanticLabel: 'search',
            ),
            onPressed: () {
              print('Search button');
            },
          ),
          IconButton(
            icon: Icon(
